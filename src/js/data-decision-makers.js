/* ═══════════════════════════════════════════════
 * DECISION MAKERS MODULE
 * Functions for looking up decision makers by state,
 * county, or opportunity type.
 * ═══════════════════════════════════════════════ */
SN.decisionMakers = {

    /**
     * Get all decision makers relevant to a county.
     * Returns state broadband director + any co-ops/tribal contacts in state.
     */
    getForCounty: function(county) {
        var results = [];
        var st = county.state;

        // State broadband office director
        var stateContact = SN.data.stateDecisionMakers[st];
        if (stateContact) {
            results.push({
                type: 'State Broadband Office',
                name: stateContact.name,
                title: stateContact.title,
                agency: stateContact.agency,
                email: stateContact.email,
                phone: stateContact.phone,
                relevance: 'BEAD subgrant authority for ' + st
            });
        }

        // Electric co-ops in state
        SN.data.coopDecisionMakers.forEach(function(coop) {
            if (coop.state === st) {
                results.push({
                    type: 'Electric Co-op',
                    name: coop.name,
                    title: coop.contact,
                    agency: coop.name,
                    phone: coop.phone,
                    relevance: coop.fiberStatus + ' fiber — ' + coop.milesPlanned.toLocaleString() + ' miles planned'
                });
            }
        });

        // Tribal contacts near this state
        SN.data.tribalDecisionMakers.forEach(function(tribal) {
            if (tribal.region.indexOf(st) >= 0) {
                results.push({
                    type: 'Tribal Administrator',
                    name: tribal.name,
                    title: tribal.contact,
                    agency: tribal.tribe,
                    phone: tribal.phone,
                    relevance: tribal.program + ' — ' + tribal.unservedHouseholds.toLocaleString() + ' unserved households'
                });
            }
        });

        // Smart city contacts in state
        if (SN.data.smartCities) {
            SN.data.smartCities.forEach(function(city) {
                if (city.state === st && city.decisionMaker) {
                    results.push({
                        type: 'Smart City CIO',
                        name: city.decisionMaker.name,
                        title: city.decisionMaker.title,
                        agency: city.name + ', ' + city.state,
                        email: city.decisionMaker.email,
                        phone: city.decisionMaker.phone,
                        relevance: city.program + ' — ' + city.status
                    });
                }
            });
        }

        return results;
    },

    /**
     * Get state broadband director for a state.
     */
    getStateDirector: function(state) {
        return SN.data.stateDecisionMakers[state] || null;
    },

    /**
     * Get all co-ops building fiber.
     */
    getAllCoops: function() {
        return SN.data.coopDecisionMakers;
    },

    /**
     * Get all tribal contacts.
     */
    getAllTribal: function() {
        return SN.data.tribalDecisionMakers;
    },

    /**
     * Get co-ops for a specific state.
     */
    getCoopsForState: function(state) {
        return SN.data.coopDecisionMakers.filter(function(c) { return c.state === state; });
    }
};
